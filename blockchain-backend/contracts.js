let provider, signer;
let certAgency, certifier, certTemplate, receiver, certificates;

const connectButton = document.getElementById("connectButton");
const walletAddressEl = document.getElementById("walletAddress");
const resultEl = document.getElementById("result");

connectButton.addEventListener("click", async () => {
    if (typeof window.ethereum !== 'undefined') {
        try {
            provider = new ethers.providers.Web3Provider(window.ethereum);
            await provider.send("eth_requestAccounts", []);
            signer = provider.getSigner();

            // --- Instantiate all 5 contracts ---
            certAgency = new ethers.Contract(certAgencyAddr, certAgencyABI, signer);
            certifier = new ethers.Contract(certifierAddr, certifierABI, signer);
            certTemplate = new ethers.Contract(certTemplateAddr, certTemplateABI, signer);
            receiver = new ethers.Contract(receiverAddr, receiverABI, signer);
            certificates = new ethers.Contract(certificatesAddr, certificatesABI, signer);

            const address = await signer.getAddress();
            walletAddressEl.innerText = `Connected: ${address}`;

            // --- Update button style on successful connection ---
            connectButton.innerText = "✅ Connected";
            connectButton.disabled = true;

        } catch (error) {
            console.error(error);
            alert("Failed to connect wallet.");
        }
    } else {
        alert("Please install a browser wallet like MetaMask!");
    }
});

// A helper function to handle sending transactions and displaying the result
async function handleTransaction(txPromise) {
    if (!signer) { alert("Please connect your wallet first!"); return; }
    try {
        resultEl.innerText = "Sending transaction...";
        const tx = await txPromise;
        resultEl.innerText = `Transaction sent! Waiting for confirmation...\nHash: ${tx.hash}`;
        await tx.wait();
        resultEl.innerText = `✅ Transaction Confirmed!\nHash: ${tx.hash}`;
    } catch (error) {
        console.error(error);
        resultEl.innerText = `❌ Error: ${error.reason || error.message}`;
    }
}


